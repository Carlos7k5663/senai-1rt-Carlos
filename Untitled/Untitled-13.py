valor1 = float( input ("coloque a primeir valor: "))
valor2 = float( input("coloque a segundo valor: ")) 
valor3 = float( input("coloque a terceiro valor: "))
 
maior_valor = max(valor1, valor2, valor3)
menor_valor = min(valor1, valor2, valor3) 

print("o maior valor digitado foi:", maior_valor)
print("o menor valor digitado foi:", menor_valor)
